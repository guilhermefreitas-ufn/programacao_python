meses = ["", "Janeiro", "Fevereiro", "Março", "Abril", "Maio", "Junho", "Julho", "Agosto", "Setembro", "Outubro", "Novembro", "Dezembro"]

valor_minimo, mes_minimo = calcular_min(lista_inflacao)
valor_maximo, mes_maximo = calcular_max(lista_inflacao)

