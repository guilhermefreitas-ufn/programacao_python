import random

def sortear_valores_inflacao(lista, quantidade):
  for i in range(quantidade):
    lista.append(random.randint(4,10))

def exibir_dados_inflacao(lista):
  for item in lista:
    print(item)

def calcular_min(lista):
  menor = lista[0]
  mes = 0

  for item in range(len(lista)):
    if lista[item] < menor:
      menor = lista[item]
      mes = item

  return menor, mes + 1

def calcular_max(lista):
  maior = lista[0]
  mes = 0

  for item in range(len(lista)):
    if lista[item] > maior:
      maior = lista[item]
      mes = item

  return maior, mes + 1

#simulando que 0 é janeiro, 1 é fevereiro, 2 é março, assim por diante
